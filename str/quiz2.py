# 문자열 "AI" 에서 인덱싱을 이용해 "A"와 "I"를 각각 출렸하세요
print("AI"[0])
print("AI"[1])

# 문자열 "Korea"에서 인덱싱을 이용해 "K"와 "a"를 각각 출력하세요
# 인덱싱이란? 인덱스번호를 사용해서 문자를 추출
# 인덱스번호
# "Korea" -> 0~4
print("Korea"[0])
print("Korea"[4])

# 문자열 "Programming"의 마지막 글자를 출력하시오
# 문자열이 너무 길어 마지만번호를 확인하기 어려울때
print(len("Programming"))
# 인덱스 번호 : 0 ~ 길이-1
print("programming"[10])

# 문자열 "Python" 에서 슬라이싱을 이용해 "Py"와 "thon"을 각각 출력하세요
print("Python"[0:2])
print("Python"[2:])