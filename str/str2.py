# 문자 데이터를 위한 연산자

# +기호
# +기호는 연산자, 2와 3은 항
# 이 식에서 +기호는 더하기 연산자
print(2+3) # 5

# 이 식에서 +기호는 문자열 연결 연산자
# 두 항이 문자면 +는 문자열 연결 연산자가 됨
print('2'+'3') # '23'      

# 파이썬 파일 실행 : f6 또는 실행버튼

# 문자 데이터를 위한 연산자
# 문자열 열결 연산자
print("2"+"3") #23

print("안녕"+"하세요") #안녕하세요

print("ab"+"cd"+"ef") #abcdef

# 문자열 반복 연산자
# 문자 * 횟수
print("a"*3) 
print("abc"*5) 

# 동물 소리 내기
print("멍"*3+"!")

